package Utils;

public class Ekiralik {

	private String ekid;
	private String ekbaslik;
	private String ekacik;
	private String ekdetay;
	private String ekresim;
	private String ekfiyat;
	public String getEkid() {
		return ekid;
	}
	public void setEkid(String ekid) {
		this.ekid = ekid;
	}
	public String getEkbaslik() {
		return ekbaslik;
	}
	public void setEkbaslik(String ekbaslik) {
		this.ekbaslik = ekbaslik;
	}
	public String getEkacik() {
		return ekacik;
	}
	public void setEkacik(String ekacik) {
		this.ekacik = ekacik;
	}
	public String getEkdetay() {
		return ekdetay;
	}
	public void setEkdetay(String ekdetay) {
		this.ekdetay = ekdetay;
	}
	public String getEkresim() {
		return ekresim;
	}
	public void setEkresim(String ekresim) {
		this.ekresim = ekresim;
	}
	public String getEkfiyat() {
		return ekfiyat;
	}
	public void setEkfiyat(String ekfiyat) {
		this.ekfiyat = ekfiyat;
	}

}
